package switchcase;

public class Switch15 {
	public static void main(String[] args)
	{
		int a=-34;
		switch(a)
		{
		case 1:System.out.println("Positive");
		break;
		case -34:System.out.println("Negative");
		break;
		case 3:System.out.println("Integer");
		break;
		default:System.out.println("Invalid");
		}
		
	}
}
