package switchcase;

public class Switch20 {
	public static void main(String[] args)
	{
		int amt=5000;
		switch(amt)
		{
		case 7000:System.out.println("2 sharing");
		break;
		case 5000:System.out.println("3 Sharing");
		break;
		case 9000:System.out.println("1 sharing");
		break;
		default:System.out.println("Invalid");
		}
	}
}
