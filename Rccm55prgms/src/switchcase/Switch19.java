package switchcase;

public class Switch19 {
	public static void main(String[] args)
	{
		int amt=50;
		switch(amt)
		{
		case 70:System.out.println("Day pass");
		break;
		case 1000:System.out.println("Monthly");
		break;
		case 350:System.out.println("Weekly");
		break;
		default:System.out.println("Invalid");
		}
	}
}
