package switchcase;

public class Switch16 {
	public static void main(String[] args)
	{
		String lang="other";
		switch(lang)
		{
		case "French":System.out.println("France");
		break;
		case "japaneese":System.out.println("Japan");
		break;
		case "English":System.out.println("US");
		break;
		default:System.out.println("India");
		}
	}
}
