package switchcase;

public class Switch18 {
	public static void main(String[] args)
	{
		String Cricket="VK";
		switch(Cricket)
		{
		case "Malinga":System.out.println("Srilanka");
		break;
		case "ABD":System.out.println("SA");
		break;
		case "VK":System.out.println("India");
		break;
		default:System.out.println("Other");
		}
	}
}
