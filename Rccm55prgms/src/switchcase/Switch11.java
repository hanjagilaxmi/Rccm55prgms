package switchcase;

public class Switch11 {
	public static void main(String[] args)
	{
		char ch='z';
		switch(ch)
		{
		case 'a':System.out.println("Red");
		break;
		case 'd':System.out.println("Green");
		break;
		case 'v':System.out.println("Black");
		break;
		case 'p':System.out.println("White");
		break;
		//default:System.out.println("Invalid");
		}
	}
}
