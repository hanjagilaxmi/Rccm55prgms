package switchcase;

public class switch2 {
	public static void main(String[] args)
	{
		int a=26;
		switch(a)
		{
		case 10:System.out.println("10");
		break;
		case 26:System.out.println("26");
		break;
		case 35:System.out.println("35");
		break;
		default:System.out.println("Invalid Grade");
		}
	}
}
