package switchcase;

public class switch7 {
	public static void main(String[] args)
	{
		int level=2;
		switch(level)
		{
		case 1:System.out.println("Beginner");
		break;
		case 2:System.out.println("Intermediate");
		break;
		case 3:System.out.println("Expert");
		break;
		default:System.out.println("0");
		}
	}

}
