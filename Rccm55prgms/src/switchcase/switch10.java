package switchcase;

public class switch10 {
	public static void main(String[] args)
	{
		int a=6;
		switch(a)
		{
		case 4:System.out.println("join to nursery");
		break;
		case 6:System.out.println("join to school");
		break;
		case 9:System.out.println("join to 3rd standard");
		break;
		default:System.out.println("Invalid");
		}
	}
}
