package switchcase;

public class Switch17 {
	public static void main(String[] args)
	{
		String IPL="VK";
		switch(IPL)
		{
		case "Hardik":System.out.println("Gujarat");
		break;
		case "MSD":System.out.println("Chennai");
		break;
		case "VK":System.out.println("RCB");
		break;
		default:System.out.println("Other");
		}
	}
}
