package Arrays;

public class array19 {
	public static void main(String[] args)
	{
		int abc[]=new int[4];
		abc[0]=23;
		abc[1]=26;
		abc[2]=76;
		abc[3]=89;
		System.out.println("index \t values");
		
		for(int i=0;i<=abc.length-1;i++)
		{
			System.out.println(i+" \t "+abc[i]);
		}
		
	}
}
