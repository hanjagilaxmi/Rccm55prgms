package esleifstatement;

public class else7 {
	public static void main(String[] args)
    {
        int i = 20;
 
        if (i == -10)
            System.out.println("Negative value");
 
        else if (i > 15)
            System.out.println("greater");
 
        else if (i < 20)
            System.out.println("less");
 
        else
            System.out.println("i is not present");
    }
}
