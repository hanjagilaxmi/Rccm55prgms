package esleifstatement;

public class else15 {
	public static void main(String[] args)
    {
        String cap = "Bangalore";
 
        if (cap == "Chennai")
            System.out.println("TN");
 
        else if (cap == "Bangalore")
            System.out.println("Karnataka");
 
        else if (cap == "Hyderabad")
            System.out.println("Telangana");
 
        else
            System.out.println("other state");
    }

}
