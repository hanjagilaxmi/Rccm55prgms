package esleifstatement;

public class else6 {
	public static void main(String[] args)
    {
        int i = 20;
 
        if (i < 10)
            System.out.println("i is less");
 
        else if (i < 15)
            System.out.println("i is less");
 
        else if (i < 20)
            System.out.println("i is less");
 
        else
            System.out.println("i is greater or equal to 20");
    }
}
