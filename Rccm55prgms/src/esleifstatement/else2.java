public class else2 {
	public static void main(String[] args)
    {
        String str = "Red";
 
        if (str == "Green")
            System.out.println("move slowly");
 
        else if (str == "Yellow")
            System.out.println("Keep ready");
 
        else if (str == "Red")
            System.out.println("Stop");
 
        else
            System.out.println("Danger");
    }

}
