package esleifstatement;

public class else18 {
	public static void main(String[] args)
    {
        int i = 20;
 
        if (i < 40)
            System.out.println("40/i");
 
        else if (i > 10)
            System.out.println("i/10");
 
        else if (i > 0)
            System.out.println("i/0");
 
        else
            System.out.println("not present");
    }
}
