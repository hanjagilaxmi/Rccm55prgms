package esleifstatement;

public class else9 {
	public static void main(String[] args)
    {
        int age = 20;
 
        if (age < 6)
            System.out.println("Child");
 
        else if (age > 18)
            System.out.println("Adult");
 
        else if (age > 65)
            System.out.println("Seniors");
 
        else
            System.out.println("not present");
    }
}
