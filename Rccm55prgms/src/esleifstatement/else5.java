package esleifstatement;

public class else5 {
	public static void main(String[] args)
    {
        char ch = 'A';
 
        if (ch == 'a')
            System.out.println("lowercase");
 
        else if (ch == 'A')
            System.out.println("Uppercase");
 
        else if (ch == '9')
            System.out.println("Number");
 
        else
            System.out.println("ch is not present");
    }
}
